<?php

/* @var $this \yii\web\View */
/* @var $content string */

use yii\helpers\Html;
use yii\bootstrap\Nav;
use yii\bootstrap\NavBar;
use yii\widgets\Breadcrumbs;
use frontend\assets\AppAsset;
use common\widgets\Alert;
use yii\widgets\Menu;

AppAsset::register($this);
?>
<?php $this->beginPage() ?>
<!DOCTYPE html>
<html lang="<?= Yii::$app->language ?>">
<head>
    <meta charset="<?= Yii::$app->charset ?>">
    <meta http-equiv="X-UA-Compatible" content="IE=edge">
    <meta name="viewport" content="width=device-width, initial-scale=1">
    <?= Html::csrfMetaTags() ?>
    <title><?= Html::encode($this->title) ?></title>
    <?php $this->head() ?>
    <style type="text/css">
        .active{
            color: red!important;
            font-weight: bold!important;
        }
    </style>
</head>
<body>
<?php $this->beginBody() ?>

<div class="wrap">
    <?php
    NavBar::begin([
        'brandLabel' => Yii::$app->name,
        'brandUrl' => Yii::$app->homeUrl,
        'options' => [
            'class' => 'navbar-inverse navbar-fixed-top',
        ],
    ]);
    $menuItems = [
        ['label' => 'Админка', 'url' => ['/admin']],
        ['label' => 'Главная', 'url' => ['/']],
        ['label' => 'О нас', 'url' => ['about']],
        ['label' => 'Контакты', 'url' => ['contact']],
    ];
    if (Yii::$app->user->isGuest) {
        $menuItems[] = ['label' => 'Регистрация', 'url' => ['signup']];
        $menuItems[] = ['label' => 'Войти', 'url' => ['login']];
    } else {
        $menuItems[] = '<li>'
            . Html::beginForm(['logout'], 'post')
            . Html::submitButton(
                'Выйти (' . Yii::$app->user->identity->username . ')',
                ['class' => 'btn btn-link logout']
            )
            . Html::endForm()
            . '</li>';
    }
    echo Nav::widget([
        'options' => ['class' => 'navbar-nav navbar-right'],
        'items' => $menuItems,
    ]);
    NavBar::end();
    ?>

    <div class="container">
        <?= Breadcrumbs::widget([
            'links' => isset($this->params['breadcrumbs']) ? $this->params['breadcrumbs'] : [],
        ]) ?>
        <?= Alert::widget() ?>

        <?


        echo Menu::widget([
            'items' => [
                ['label' => 'Главная', 'url' => ['/']],
                ['label' => 'О нас', 'url' => ['/about'], 'active' => \Yii::$app->request->get('/about')],
                ['label' => 'Услуги', 'url' => ['/services']],
                ['label' => 'Контакты', 'url' => ['/contacts']],
            ],
            'options' => [
                'id'=>'menu',
                'class' => 'menu nav navbar-nav navbar-right',
                'data-id'=>'menu',
            ],
            'activeCssClass'=>'active',
            'firstItemCssClass'=>'fist1',
            'lastItemCssClass' =>'last2',
        ]);


        ?>

        <?= $content ?>
    </div>
</div>

<footer class="footer">
    <div class="container">
        <p class="pull-left">&copy; <?= Html::encode(Yii::$app->name) ?> <?= date('Y') ?></p>

        <p class="pull-right"><?= Yii::powered() ?></p>
    </div>
</footer>

<?php $this->endBody() ?>
</body>
</html>
<?php $this->endPage() ?>

